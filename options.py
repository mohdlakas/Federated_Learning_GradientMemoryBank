#!/usr/bin/env python
# -*- coding: utf-8 -*-
# Python version: 3.6

import argparse


def args_parser():
    parser = argparse.ArgumentParser()

    # federated arguments (Notation for the arguments followed from paper)
    """
    Federated Learning Parameters
    The file defines several core federated learning hyperparameters that directly correspond to 
    the FedAvg algorithm notation:

    --epochs (default: 10): Number of global communication rounds between server and clients
    --num_users (default: 100): Total number of simulated clients in the federation (K)
    --frac (default: 0.25): Fraction of clients selected per round (C) - only 25% participate each round
    --local_ep (default: 2): Local epochs each client trains before sending updates (E)
    --local_bs (default: 10): Batch size for local client training (B 
    """
    parser.add_argument('--epochs', type=int, default=10,
                        help="number of rounds of training")
    parser.add_argument('--num_users', type=int, default=100,
                        help="number of users: K")
    parser.add_argument('--frac', type=float, default=0.1,
                        help='the fraction of clients: C')
    parser.add_argument('--local_ep', type=int, default=2,
                        help="the number of local epochs: E")
    parser.add_argument('--local_bs', type=int, default=10,
                        help="local batch size: B")
    parser.add_argument('--lr', type=float, default=0.01,
                        help='learning rate')
    parser.add_argument('--momentum', type=float, default=0.5,
                        help='SGD momentum (default: 0.5)')

    # FedProx specific
    parser.add_argument('--mu', type=float, default=0.01,
                        help='proximal term parameter for FedProx')
    

    # model arguments
    """     
    Model Architecture Configuration
    The parser includes detailed neural network architecture parameters:

    --model: Model type (CNN, MLP, etc.)
    --kernel_sizes: Convolutional kernel dimensions (e.g., "3,4,5")
    --num_channels: Input channels (1 for grayscale, 3 for RGB)
    --num_filters: Number of convolutional filters
    --norm: Normalization type (batch_norm, layer_norm, or None) 
    """

    parser.add_argument('--model', type=str, default='cnn', help='model name')
    parser.add_argument('--kernel_num', type=int, default=9,
                        help='number of each kind of kernel')
    parser.add_argument('--kernel_sizes', type=str, default='3,4,5',
                        help='comma-separated kernel size to \
                        use for convolution')
    parser.add_argument('--num_channels', type=int, default=1, help="number \
                        of channels of imgs")
    parser.add_argument('--norm', type=str, default='batch_norm',
                        help="batch_norm, layer_norm, or None")
    parser.add_argument('--num_filters', type=int, default=32,
                        help="number of filters for conv nets -- 32 for \
                        mini-imagenet, 64 for omiglot.")
    parser.add_argument('--max_pool', type=str, default='True',
                        help="Whether use max pooling rather than \
                        strided convolutions")

    # other argument
    
    parser.add_argument('--gpu_id', type=int, default=None, help="GPU ID to use")

    parser.add_argument('--dataset', type=str, default='mnist', help="name \
                        of dataset")
    parser.add_argument('--num_classes', type=int, default=10, help="number \
                        of classes")
    parser.add_argument('--gpu', default=None, help="To use cuda, set \
                        to a specific GPU ID. Default set to use CPU.")
    parser.add_argument('--optimizer', type=str, default='sgd', help="type \
                        of optimizer")
    parser.add_argument('--iid', type=int, default=1,
                        help='Default set to IID. Set to 0 for non IID.')
    parser.add_argument('--unequal', type=int, default=0,
                        help='whether to use unequal data splits for  \
                        non-i.i.d setting (use 0 for equal splits)')
    parser.add_argument('--stopping_rounds', type=int, default=10,
                        help='rounds of early stopping')
    parser.add_argument('--verbose', type=int, default=1, help='verbose')
    parser.add_argument('--seed', type=int, default=1, help='random seed')
    args = parser.parse_args()
    return args
